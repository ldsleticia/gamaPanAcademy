package br.com.panacademycondicionais.whileefor;

public class Contador {
    public static void main(String[] args) {
        for (int i = 2; i <= 2; i++) {
            //MOSTRAR A FRASE NA TELA 2X
            System.out.println("BATATINHA FRITA 123 = ");
        }
        int k = 0;
        while (k <= 2) {
            System.out.println("BATATINHA FRITA 123 = ");
            k++;
        }
    }
}
